package com.mavioliveira.workshopMongoDB.domain;

import org.springframework.data.annotation.Id;
import org.springframework.data.mongodb.core.mapping.DBRef;
import org.springframework.data.mongodb.core.mapping.Document;

import java.io.Serializable;
import java.util.ArrayList;
import java.util.List;
import java.util.Objects;


@Document(collection = "user") // informa que a classe corresponde a uma coleção do MongoDB, funciona sem a definição do collection, mas é bom colocar

public class User implements Serializable {

        @Id
        private String id;
        private String name;
        private String email;

        @DBRef (lazy = true)// post vira referencia no user, isso garante que os post só serão carregados se forem acessadoss
        private List<Post> posts = new ArrayList<>();  // coleção de post(id)

    public User() {
    }

    public User(String id, String name, String email) {
        this.id = id;
        this.name = name;
        this.email = email;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public List<Post> getPosts() {
        return posts;
    }

    public void setPosts(List<Post> posts) {
        this.posts = posts;
    }

    public void setEmail(String email) {
        this.email = email;


    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        User user = (User) o;
        return Objects.equals(id, user.id);
    }

    @Override
    public int hashCode() {
        return Objects.hashCode(id);
    }
}
